# type shows the type/class of the object or variable we pass as argument

# defining the variables
tuple = ('cola', 'cola', 'cola')

diccionary = {
    'name': 'fran',
    'age': 17,
    'education_level': 'high',
}

list = ['list', 'random', 'value']

# definiing the type of the variables
type_of_dictionary = type(diccionary)
type_of_list = type(list)
type_of_tuple = type(tuple)

# printing the type of the variables
print(type_of_tuple)
print(type_of_dictionary)
print(type_of_list)
